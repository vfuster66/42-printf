/****************************************************************************
 *
 * 
 * 
*****************************************************************************/
int	ft_plen(unsigned long int n)
{
	int	len;

	len = 0;
	if (n == 0)
		return (1);
	while (n >= 1)
	{
		len++;
		n /= 16;
	}
	return (len);
}

/****************************************************************************
 *
 * 
 * 
*****************************************************************************/
void	ft_print_address(unsigned long int n)
{
	if (n >= 16)
	{
		ft_print_address(n / 16);
		ft_print_address(n % 16);
	}
	else
	{
		if (n < 10)
			ft_print_c(n + '0');
		else if (n >= 10)
			ft_print_c((n - 10) + 'a');
	}
}

/****************************************************************************
 *
 * 
 * 
*****************************************************************************/
int	ft_print_p(unsigned long int n)
{
	int	count;

	count = 0;
	if (n == 0)
	{
		count += ft_print_s("(nil)");
		return (count);
	}
	count += ft_print_s("0x");
	ft_print_address(n);
	count += ft_plen(n);
	return (count);
}

/****************************************************************************
 *
 * 
 * 
*****************************************************************************/
int	ft_print_ptr(unsigned long int n, t_flag flag)
{
	int	count;

	count = 0;
	if (n == 0)
		flag.width -= ft_strlen("(nil)") - 1;
	else
		flag.width -= 2;
	if (flag.left == 1)
		count += ft_print_p(n);
	count += ft_width(flag.width, ft_plen(n), 0);
	if (flag.left == 0)
		count += ft_print_p(n);
	return (count);
}
